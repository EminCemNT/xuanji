// 评分计算（seed 已包含日期+星座+周期偏移量）
export function getFortuneScores(seed) {
  return {
    love: (seed % 5) + 1,
    career: ((seed * 7) % 5) + 1,
    wealth: ((seed * 13) % 5) + 1,
    health: ((seed * 17) % 5) + 1
  }
}

// 速配星座
